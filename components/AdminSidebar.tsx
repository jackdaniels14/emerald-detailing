export default function AdminSidebar() {
  const pathname = usePathname();
  const { userProfile, signOut } = useAuth();
  const { mode, setMode } = useConsoleMode();
  const [expandedGroups, setExpandedGroups] = useState<string[]>(['Operations', 'Work', 'Insight', 'Me', 'Leads', 'Quick Filters']);

  // Check if user has access to Sales CRM
  const canAccessSales = hasMinRole(userProfile?.role, 'sales_rep');

  // Auto-switch mode based on current path
  useEffect(() => {
    if (pathname.startsWith('/admin/sales')) {
      setMode('sales');
    } else if (pathname.startsWith('/admin') && !pathname.startsWith('/admin/sales')) {
      // Only switch to control if we're not on a sales page
      // This prevents flashing when navigating within sales
    }
  }, [pathname, setMode]);

  const toggleGroup = (groupName: string) => {
    setExpandedGroups(prev =>
      prev.includes(groupName)
        ? prev.filter(g => g !== groupName)
        : [...prev, groupName]
    );
  };

  // Get current navigation based on mode
  const soloItems = mode === 'sales' ? salesSoloItems : controlSoloItems;
  const navGroups = mode === 'sales' ? salesNavGroups : controlNavGroups;

  // Filter solo items based on permissions
  const filteredSoloItems = soloItems.filter(item =>
    userProfile ? hasAccessToPath(userProfile.role, item.href) : false
  );

  // Filter groups and their items based on permissions
  const filteredGroups = navGroups
    .map(group => ({
      ...group,
      items: group.items.filter(item =>
        userProfile ? hasAccessToPath(userProfile.role, item.href) : false
      ),
    }))
    .filter(group => group.items.length > 0);

  const isItemActive = (href: string) => {
    if (mode === 'sales' && href === '/admin/sales') {
      return pathname === '/admin/sales';
    }
    if (mode === 'control' && href === '/admin') {
      return pathname === '/admin';
    }
    return pathname === href || (href !== '/admin' && href !== '/admin/sales' && pathname.startsWith(href));
  };

  const isGroupActive = (group: NavGroup) => {
    return group.items.some(item => isItemActive(item.href));
  };

  // Theme colors based on mode
  const bgColor = mode === 'sales' ? 'bg-slate-900' : 'bg-gray-900';
  const headerBgColor = mode === 'sales' ? 'bg-slate-800' : 'bg-gray-800';
  const accentColor = mode === 'sales' ? 'text-blue-400' : 'text-emerald-400';
  const activeBgColor = mode === 'sales' ? 'bg-blue-600' : 'bg-emerald-600';

  return (
    <div className={`flex flex-col h-full ${bgColor} w-64`}>
      {/* Logo / Mode Switcher */}
      <div className={`h-16 px-4 ${headerBgColor}`}>
        {canAccessSales ? (
          <div className="h-full flex items-center">
            <div className="flex-1 flex bg-gray-700/50 rounded-lg p-1">
              <button
                onClick={() => setMode('control')}
                className={`flex-1 flex items-center justify-center gap-2 px-3 py-1.5 rounded-md text-sm font-medium transition-all ${
                  mode === 'control'
                    ? 'bg-emerald-600 text-white shadow-sm'
                    : 'text-gray-400 hover:text-white'
                }`}
              >
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2H6a2 2 0 01-2-2V6zM14 6a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2h-2a2 2 0 01-2-2V6zM4 16a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2H6a2 2 0 01-2-2v-2zM14 16a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2h-2a2 2 0 01-2-2v-2z" />
                </svg>
                Control
              </button>
              <button
                onClick={() => setMode('sales')}
                className={`flex-1 flex items-center justify-center gap-2 px-3 py-1.5 rounded-md text-sm font-medium transition-all ${
                  mode === 'sales'
                    ? 'bg-blue-600 text-white shadow-sm'
                    : 'text-gray-400 hover:text-white'
                }`}
              >
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 7h8m0 0v8m0-8l-8 8-4-4-6 6" />
                </svg>
                Sales
              </button>
            </div>
          </div>
        ) : (
          <div className="h-full flex items-center">
            <Link href="/admin" className="flex items-center">
              <span className="text-xl font-bold text-emerald-400">Emerald</span>
              <span className="text-xl font-bold text-white ml-1">Admin</span>
            </Link>
          </div>
        )}
      </div>

      {/* Mode Label */}
      <div className={`px-4 py-2 border-b ${mode === 'sales' ? 'border-slate-700' : 'border-gray-700'}`}>
        <p className={`text-xs font-semibold uppercase tracking-wider ${accentColor}`}>
          {mode === 'sales' ? 'Sales CRM' : 'Control Center'}
        </p>
      </div>

      {/* Navigation */}
      <nav className="flex-1 px-2 py-4 space-y-1 overflow-y-auto">
        {/* Solo Items (Dashboard) */}
        {filteredSoloItems.map((item) => {
          const isActive = isItemActive(item.href);
          return (
            <Link
              key={item.name}
              href={item.href}
              className={`flex items-center px-3 py-2 text-sm font-medium rounded-lg transition-colors ${
                isActive
                  ? `${activeBgColor} text-white`
                  : 'text-gray-300 hover:bg-gray-800 hover:text-white'
              }`}
            >
              <span className={isActive ? 'text-white' : 'text-gray-400'}>{item.icon}</span>
              <span className="ml-3">{item.name}</span>
            </Link>
          );
        })}

        {/* Grouped Items */}
        {filteredGroups.map((group) => {
          const isExpanded = expandedGroups.includes(group.name);
          const groupActive = isGroupActive(group);

          return (
            <div key={group.name} className="pt-2">
              {/* Group Header */}
              <button
                onClick={() => toggleGroup(group.name)}
                className={`w-full flex items-center justify-between px-3 py-2 text-sm font-medium rounded-lg transition-colors ${
                  groupActive
                    ? accentColor
                    : 'text-gray-400 hover:text-gray-300'
                }`}
              >
                <div className="flex items-center">
                  <span className={groupActive ? accentColor : 'text-gray-500'}>{group.icon}</span>
                  <span className="ml-3 uppercase text-xs tracking-wider">{group.name}</span>
                </div>
                <svg
                  className={`w-4 h-4 transition-transform ${isExpanded ? 'rotate-180' : ''}`}
                  fill="none"
                  stroke="currentColor"
                  viewBox="0 0 24 24"
                >
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                </svg>
              </button>

              {/* Group Items */}
              {isExpanded && (
                <div className={`mt-1 ml-4 space-y-1 border-l ${mode === 'sales' ? 'border-slate-700' : 'border-gray-700'} pl-3`}>
                  {group.items.map((item) => {
                    const isActive = isItemActive(item.href);
                    return (
                      <Link
                        key={item.name}
                        href={item.href}
                        className={`flex items-center px-3 py-2 text-sm font-medium rounded-lg transition-colors ${
                          isActive
                            ? `${activeBgColor} text-white`
                            : 'text-gray-300 hover:bg-gray-800 hover:text-white'
                        }`}
                      >
                        <span className={isActive ? 'text-white' : 'text-gray-400'}>{item.icon}</span>
                        <span className="ml-3">{item.name}</span>
                      </Link>
                    );
                  })}
                </div>
              )}
            </div>
          );
        })}
      </nav>

      {/* User section */}
      <div className={`p-4 border-t ${mode === 'sales' ? 'border-slate-800' : 'border-gray-800'}`}>
        <div className="flex items-center">
          <div className="flex-shrink-0">
            <div className={`w-8 h-8 rounded-full ${mode === 'sales' ? 'bg-blue-500' : 'bg-emerald-500'} flex items-center justify-center`}>
              <span className="text-white text-sm font-medium">
                {userProfile?.firstName?.charAt(0) || 'A'}
              </span>
            </div>
          </div>
          <div className="ml-3 flex-1 min-w-0">
            <p className="text-sm font-medium text-white truncate">
              {userProfile?.firstName} {userProfile?.lastName}
            </p>
            <p className="text-xs text-gray-400 truncate">
              {userProfile?.role ? getRoleDisplayName(userProfile.role) : 'Admin'}
            </p>
          </div>
          <button
            onClick={() => signOut()}
            className="ml-2 p-1 text-gray-400 hover:text-white transition-colors"
            title="Sign out"
          >
            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
          </button>
        </div>
      </div>
    </div>
  );
}
